val dia = 3

when (dia) {
    1 -> println("Hoy es lunes. Comienza la semana de trabajo.")
    2 -> println("Hoy es martes. Es un buen día para avanzar en tus proyectos.")
    3 -> println("Hoy es miércoles. ¡Es mitad de semana!")
    4 -> println("Hoy es jueves. ¡Casi es viernes!")
    5 -> println("Hoy es viernes. ¡El fin de semana está cerca!")
    6 -> println("Hoy es sábado. Tiempo para relajarse o divertirse.")
    7 -> println("Hoy es domingo. Prepárate para la semana siguiente.")
    else -> println("Valor de día inválido. Debe ser un número entre 1 y 7.")
    }