// Define la cadena de texto original
val originalString = "Hola, Kotlin es genial!"

// Define el carácter o subcadena a reemplazar y el nuevo carácter o subcadena
val targetSubstring = "genial"
val replacementSubstring = "fantástico"

// Reemplaza la subcadena en la cadena original
val modifiedString = originalString.replace(targetSubstring, replacementSubstring)

// Imprime la cadena modificada
println("La cadena modificada es: $modifiedString")
