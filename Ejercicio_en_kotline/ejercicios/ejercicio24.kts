// Definir una cadena
val cadena = "Hola, Mundo!"

// Convertir la cadena a minúsculas
val cadenaMinusculas = cadena.toLowerCase()

// Convertir la cadena a mayúsculas
val cadenaMayusculas = cadena.toUpperCase()

// Imprimir las cadenas convertidas
println("Cadena original: $cadena")
println("Cadena en minúsculas: $cadenaMinusculas")
println("Cadena en mayúsculas: $cadenaMayusculas")

